Verizon.GlobalMenu = (function () {
	'use strict';
	var GlobalMenu, menuData, mainMenu, linkTitle, linkUrl, menuItem, li, copyMenu;
	// Global menu namespace
	GlobalMenu = {};

  GlobalMenu.factory = function() {
    // fetch menu data
		menuData = getGlobalHeaderMenu();
		// render main menu
		mainMenu = document.createElement('UL');
		mainMenu.className = 'nav navbar-nav';
		
		for(var i = 0; i < menuData.length; i++) {
			menuItem = menuData[i];
			linkTitle = document.createTextNode(menuItem.title);
			linkUrl = document.createElement('A');
			if (menuItem.href !== null){
				linkUrl.setAttribute('href',menuItem.href); /* else */
			}
			if (menuItem.newWindow){
				linkUrl.setAttribute('target','_blank');
			}
			linkUrl.appendChild(linkTitle);
			li = document.createElement('LI');
			// handle if there is a submenu for dropdown
			li.appendChild(linkUrl);
			if (menuItem.submenu !== null) {
				li.className = 'dropdown';
				li.appendChild(GlobalMenu.buildDropDownMenu(menuData[i].submenu));
			}
			if (menuItem.active) {
				GlobalMenu.addClasses(li,menuItem.classes);
			}
			mainMenu.appendChild(li);
		}
		return mainMenu;
  };

	/* @function buildMenu
	 * @description: Function to iterate through the global menu object and produce the menu hierarchy structure
	 */
	GlobalMenu.buildMenu = function() {
		mainMenu = this.factory();
		// add the menu to the page
		document.getElementById('navmenu').appendChild(mainMenu);
		copyMenu = mainMenu.cloneNode(true);
		
		
	};

	/* @function buildDropDownMenu
	 * @description: Function to iterate through menu object and construct a dropdown menu structure
	 */
	GlobalMenu.buildDropDownMenu = function(menuData) {
		// calculate total elements and total menus with submenus
		var totalCount = 0, columnCount = 0, currentColumn = 0, i, ulist, list, div, columnDiv, columnDistribution,
				appliedTotal, menuSize, targetTotal;
		for(i = 0; i < menuData.length; i++) {
			totalCount += 1;
			if (menuData[i].submenu !== null) {
				totalCount += menuData[i].submenu.length;
				columnCount += 1;
			}
		}
		if (columnCount === 0) columnCount = 1;
		if (columnCount > 5) columnCount = 5;
		targetTotal = Math.floor(totalCount / columnCount);

		// render the dropdown menu container wrapper
		ulist = document.createElement('UL');
		ulist.className = 'dropdown-menu fullwidth';
		list = document.createElement('LI');
		list.className = 'dropdown-content withdesc';
		ulist.appendChild(list);
		div = document.createElement('DIV');
		div.className = 'row-eq-height';
		list.appendChild(div);
				
		// render the columns
		// logic to distribute the column's for bootstrap col's division
		columnDiv = document.createElement('DIV');
		if (menuData.length <= 2) {
			columnDistribution = 6;
		} else if (menuData.length <= 5) {
			columnDistribution = 3;
		} else {
			columnDistribution = 15;
		}
		columnDiv.className = 'col-sm-' + columnDistribution;
		div.appendChild(columnDiv);
		appliedTotal = 0;
		for (i = 0; i < menuData.length; i++) {
			menuSize = 1;
			if (menuData[i].submenu !== null){
				menuSize += menuData[i].submenu.length;
			}
			if (appliedTotal >= targetTotal && currentColumn + 1 < columnCount) {
				currentColumn++;
				appliedTotal = 0;
				columnDiv = document.createElement('DIV');
				columnDiv.className = 'col-sm-' + columnDistribution;
				div.appendChild(columnDiv);
			}
			GlobalMenu.buildMenuColumn(columnDiv, menuData[i]);
			appliedTotal += menuSize;
		}
		// return the dropdown menu container
		return ulist;
	};

	/* @function buildMenuColumn
	 * @description: Function to fetch and associate the menu links
	 */
	GlobalMenu.buildMenuColumn = function(columnDiv, menuData) {
		var titleText, h3;
		h3 = document.createElement('H3');
		h3.className = 'title';
		linkTitle = document.createTextNode(menuData.title);
		linkUrl = document.createElement('A');
		if ( (menuData.href !== null) && (menuData.href !== '') && (menuData.href !== undefined) ) {
			linkUrl.setAttribute('href',menuData.href); /* else */
			if (menuData.newWindow){
				linkUrl.setAttribute('target','_blank');
			} 
			linkUrl.appendChild(linkTitle);
			h3.appendChild(linkUrl);

		} else {
			titleText = document.createTextNode(menuData.title);		
			h3.appendChild(titleText);
		}
		
		GlobalMenu.addClasses(h3, menuData.classes);
		columnDiv.appendChild(h3);
		if (menuData.submenu !== null) {
			GlobalMenu.buildSubmenu(columnDiv, menuData.submenu);
		}
	};

	/* @function buildSubmenu
	 * @description: Function to iterate through the menu object and constructs the submenu structure
	 */
	GlobalMenu.buildSubmenu = function(columnDiv, menuData) {
		var ul, t, a, li;
		ul = document.createElement('UL');
		for (var i = 0; i < menuData.length; i++) {
			t = document.createTextNode(menuData[i].title);
			a = document.createElement('A');
			if (menuData[i].href !== null) {
				a.setAttribute('href',menuData[i].href); 
			}
			if (menuData[i].newWindow){
				a.setAttribute('target','_blank');	
			}
			GlobalMenu.addClasses(a,menuData[i].classes);
			a.appendChild(t);
			li = document.createElement('LI');
			li.appendChild(a);
			ul.appendChild(li);
		}
		columnDiv.appendChild(ul);
	};

	/* @function addClasses
	 * @description: Function eliminate null values within class elements
	 */
	GlobalMenu.addClasses = function(element, classes) {
		if (classes !== null) {
			if (element.className !== null) {
				element.className += ' ' + classes;
			}
			else {
				element.className = classes;
			} 
		}
	};


	return GlobalMenu;
})();
